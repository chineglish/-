				<p style="text-indent:2em;">（三）申购</p>
				<p style="text-indent:2em;">1、封闭期结束后，每个工作日投资者可进行主动申购，个人投资者首次申购金额为1万元，以0.1万元整数倍追加。机构投资者首次申购金额为100万元，以10万元的整数倍递增。单一个人投资者和机构投资者产品持有余额均不得超过1000万元，投资者如有大额申购需求，可另行向产品管理人进行申请。开放时间（9:00-15:00）内申购，申购申请后的第一个开放日进行申购确认，并进行扣款；自产品开放日起非开放时间可提出预约申购申请，等同在下一开放时间内的申购申请。</p>
